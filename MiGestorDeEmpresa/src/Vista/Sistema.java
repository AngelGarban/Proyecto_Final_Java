    private void btnEliminarClienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarClienteActionPerformed
        if (!"".equals(txtIdCliente.getText())) {
            int pregunta = JOptionPane.showConfirmDialog(null, "¿Esta seguro de eliminar?");
            if (pregunta == 0) {
                int id = Integer.parseInt(txtIdCliente.getText());
                client.EliminarCliente(id);
                LimpiarTable();
                LimpiarCliente();
                ListarCliente();

            }
        }
    }//GEN-LAST:event_btnEliminarClienteActionPerformed

    private void btnModificarClienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarClienteActionPerformed
        if ("".equals(txtIdCliente.getText())) {
            JOptionPane.showMessageDialog(null, "Seleccione una fila");

        } else {

            if (!"".equals(txtIdCliente.getText()) && !"".equals(jTextField2.getText()) && !"".equals(jTextField3.getText())) {
                cl.setId(Integer.parseInt(txtIdCliente.getText()));
                cl.setRazonSocial(jTextField2.getText());
                cl.setCuilCuit(Integer.parseInt(jTextField1.getText()));
                cl.setTelefono(Integer.parseInt(jTextField3.getText()));
                cl.setDireccion(jTextField4.getText());
                cl.setCorreo(jTextField5.getText());
                cl.setContacto(jTextField6.getText());
                client.ModificarCliente(cl);
                LimpiarTable();
                LimpiarCliente();
                ListarCliente();
            } else {
                JOptionPane.showMessageDialog(null, "Los campos estan vacíos");
            }
        }
    }//GEN-LAST:event_btnModificarClienteActionPerformed

