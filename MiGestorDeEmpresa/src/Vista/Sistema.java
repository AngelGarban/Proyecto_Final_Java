    private void jButton22ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton22ActionPerformed
        // BOTON GUARDAR PEDIDO
        
        
        String nombreCliente = cbxCliente.getSelectedItem().toString();
        
        int cant40 = Integer.parseInt(txtCant40.getText());
        int cant60 = Integer.parseInt(txtCant60.getText());
        int cant90 = Integer.parseInt(txtCant90.getText());
        int sal40 = -cant40;
        int sal60 = -cant60;
        int sal90 = -cant90;
        int nPedid = 1000;
        String Stat = "Pendiente";

        //pedid.setFecha((Date.valueOf(fecha)));
        pedid.setNPedido(nPedid);
        pedid.setClientePedido(nombreCliente);                
        pedid.setCantidad40(cant40);
        pedid.setCantidad60(cant60);
        pedid.setCantidad90(cant90);
        pedid.setSaldo40(sal40);
        pedid.setSaldo60(sal60);
        pedid.setSaldo90(sal90);
        pedid.setStatus(Stat);
        pedidDao.RegistrarPedido(pedid);
        
        
        LimpiarTable();
        LimpiarPedido();
        ListarPedido();
        
        JOptionPane.showMessageDialog(null, "Pedido Registrado");

        
        
    }//GEN-LAST:event_jButton22ActionPerformed

